public class GeoPoint {

    public GeoPoint(Double lat, Double lon) {
        this.lat = lat
        this.lon = lon
    }

    private Double lat
    private Double lon

    public Double getLat() {
        lat
    }

    public Double getLon() {
        lon
    }

}
